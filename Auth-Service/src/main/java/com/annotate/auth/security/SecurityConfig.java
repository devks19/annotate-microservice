    @Bean
    public SecurityFilterChain securityFilterChain(HttpSecurity http) throws Exception {
        http
//                .cors(cors -> cors.configurationSource(corsConfigurationSource()))
                .csrf(csrf -> csrf.disable())
                .sessionManagement(session ->
                        session.sessionCreationPolicy(SessionCreationPolicy.STATELESS)
                )
                .authorizeHttpRequests(auth -> auth
                        .requestMatchers("/api/auth/**").permitAll()
                        .requestMatchers("/uploads/**").permitAll()
//                        .requestMatchers("/api/videos/**").permitAll()
                        .requestMatchers("/api/feedback/**").permitAll()
//                        .requestMatchers("/api/teams/**").permitAll()
                        .requestMatchers("/api/users/creators/**").permitAll()
                                .requestMatchers("/api/users/**").permitAll()
                        .requestMatchers("/api/videos/published").permitAll()
                                .requestMatchers("/api/users/creators/{id}").permitAll()

                        .requestMatchers("/api/video-access/**").authenticated()
                        .requestMatchers("/api/users/accessible-creators").authenticated() // Specific before wildcard
//                        .requestMatchers("/api/video-access/**").authenticated()
                        .requestMatchers("/api/video-access-code/**").authenticated()
                        .anyRequest().authenticated()
                );

        http.addFilterBefore(jwtAuthenticationFilter, UsernamePasswordAuthenticationFilter.class);

        return http.build();
    }


